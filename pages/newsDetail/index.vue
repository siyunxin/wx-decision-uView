					姜宁）抓牢招商生命线，推动高质量发展。继3月27日至4月2日金普新区代表团赴渝川琼粤等地开展招商活动后，4月22日至23日，市委常委、金普新区党工委书记、管委会主任李鹏宇再次率队赴京开展招商活动，取得一系列丰硕成果，推进一批重点项目加速落地金普。新区领导赵东及相关部门人员参加招商活动。

					　　在京期间，金普新区代表团先后拜访考察万达集团、北京字节跳动科技有限公司、海南云免实业有限公司并会谈，与东方明珠文化产业基金方面会谈，实质推进了万达广场商业综合体、火山引擎创新中心、融创及海南云免免税岛、电子体育Z社区等重点项目。

					　　万达集团与金普新区多年来保持着良好合作关系。4月14日，万达集团首席总裁助理兼发展中心总经理柏雪峰到访新区时，便代表董事长王健林向李鹏宇发出诚挚邀约。4月22日下午，刚下飞机的李鹏宇一行拜访了万达集团，与王健林进行了会谈。

					　　会谈中，李鹏宇详细介绍了金普新区肩负的责任使命、近年来经济社会发展情况和城山头国家级自然保护区等文旅资源，以及新区在文化旅游领域的产业基础和土地储备优势，并着重介绍了新区启动机构改革后焕发的生机与活力。李鹏宇说，万达集团是现代服务业领军企业，创造了诸多商业奇迹。王健林董事长对大连有着特殊情怀，对大连城市建设作出重要贡献。非常期待万达集团能与金普新区进一步加强合作，在更大领域、更深层次寻找新机遇，使双方合作再上新水平。

					　　王健林对金普新区在经济发展方面取得的成绩给予高度评价，表示新区良好的工作状态、精神面貌和积极谋发展、务实进取的工作态度令他深受感动，并介绍了万达集团商业、文旅、体育等优势产业，以及在5G、生物制药、特色小镇、红色旅游等新领域的发展情况。王健林说，万达集团将与金普新区一同推进商业综合体项目进展，并谋划更多领域的项目合作。

					　　会谈中，双方还就文旅项目建设、国际一流体育赛事落地、5G产业园区开发、医疗设备研发生产等进行深入交流，并就在金普新区的金州和小窑湾区域建设万达广场达成一致意见。双方约定，将建立工作联络机制，共同落实本次会谈成果。

					　　4月23日上午，李鹏宇一行拜访考察了北京字节跳动科技有限公司，与高级商务总监樊超进行会谈。

					　　在听取字节跳动发展历程、产品技术优势和火山引擎意向在金普新区布局的城市文旅APP服务、直播带货基地、城市大脑建设、实体制造业智能化数字化解决方案等项目计划的介绍后，李鹏宇说，字节跳动作为“热点”科技企业，近年来取得的成绩有目共睹。新区有着良好的开放基础和人才基础，近年来，随着“中国传媒岛”规划、中传未来、辽宁出版集团C立方及版权交易中心、网红电竞等项目相继落地和实施，新区已成为数字科技产业的发展沃土。在践行辽宁省“数字辽宁、智造强省”发展战略中，新区也需要像字节跳动这样的领军企业来支撑发展，双方具有广阔合作空间。同时，新区在直播、网红方面的人才优势、丰富的高校资源等，都将助力字节跳动在新区的战略布局。

					　　樊超表示，字节跳动将积极研究拓宽投资领域，进一步细化项目方案，谋划打造金普新区的科创标杆项目，为新区实现数字产业化、产业数字化作出贡献。

					　　双方达成共识，将共同探讨在金普新区搭建合作平台，导入字节跳动AI技术和服务资源，为新区产业赋能，为新区企业数字化智能化提升转型赋能。

					　　4月23日上午，李鹏宇还与海南云免实业有限公司董事长王为民深入洽谈。 　

					　　海南云免的总部设在海南，拥有多家免税实体店，计划未来在全国选择30个适合的城市，落地更多免税店或免税城项目。

					　　李鹏宇详细介绍了大连自贸片区及跨境电商产业发展情况，以及金普新区作为中日（大连）地方发展合作示范区核心区、进口贸易示范区和东北地区最大的国际贸易港口口岸所拥有的商品和贸易发展优势及机遇。李鹏宇说，新区发展势头强劲，自然环境、文旅和美食、商业氛围等均在东北地区名列前茅。目前，在小窑湾区域，产业、项目正纷纷进驻，建设如火如荼，希望海南云免在未来布局中，能重点考虑大连金普新区，并将小窑湾区域列为重点考察对象。

					　　王为民表示，通过介绍，深刻感受到金普新区良好的产业基础、营商环境和极富潜力的发展机遇，将把握投资发展时机，与新区一起，联合融创集团，共同研讨国家针对免税产业的制度法规，共同推进在新区布局免税产业业态。

					　　光大金控、融创集团相关人员也参加了会谈，并就对日经贸合作、康养产业发展、金融合作等方面进行了交流。

					　　在京期间，李鹏宇还与东方明珠文化产业基金CEO朱博进行了会谈，听取了即将落户金普新区的电子体育Z社区项目规划设计方案汇报。该项目计划投资15亿元，建设内容有顶级电竞综合馆、电子体育赛事中心及直播转播中心、电竞融媒体中心、电竞教育培训及实训基地、游戏周边产品开发、电竞创业孵化基地及电竞主题酒店等。

					　　李鹏宇希望东方明珠方面加快项目实施步伐。他说，小窑湾区域现已成为金普新区投资者纷至沓来的投资热土，电子体育Z社区项目的落户，对丰富小窑湾区域的商业、文旅产业业态和聚拢人气具有积极意义。新区正持续提升行政效能、优化营商环境，致力留住人才；各相关部门将围绕项目需要，积极主动提供优质服务，确保项目各项工作顺利如期完成。

					　　东方明珠文化产业基金自2018年起全面布局电子竞技市场，并牵头成立电子竞技联盟-G联盟，旗下游戏风云G联赛是国内首个电子竞技电视联赛。

